SELECT A.PONUM,
       A.STATUS,
       B.ITEMNUM,
       B.STORELOC
FROM PO A
LEFT JOIN POLINE  B
ON (A.PONUM = B.PONUM)
WHERE (B.ITEMNUM = '1305020305' or
B.ITEMNUM = '3108021000' or
B.ITEMNUM = '4006009940' or
B.ITEMNUM = '4006009980' or
B.ITEMNUM = '4007050480') AND (B.STORELOC = '18') AND (STATUS != 'CLOSE')