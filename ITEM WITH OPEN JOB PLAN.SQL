SELECT A.JPNUM,
       A.STATUS,
       B.ITEMNUM,
       B.LOCATION
       
FROM JOBPLAN A
LEFT JOIN JOBMATERIAL B
ON (A.JPNUM = B.JPNUM)
WHERE (B.ITEMNUM = '4614000300' OR
B.ITEMNUM = '5506036560' OR
B.ITEMNUM = '5506036900' OR
B.ITEMNUM = '5506051620' OR
B.ITEMNUM = '5506071200') AND (B.LOCATION = '94') AND (A.STATUS != 'CLOSE')
