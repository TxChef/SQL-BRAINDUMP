SELECT A.JPNUM,
       A.STATUS,
       B.ITEMNUM,
       B.LOCATION
FROM JOBPLAN A
LEFT JOIN MAXIMO.JOBMATERIAL B
ON (A.JPNUM = B.JPNUM)
WHERE (B.ITEMNUM = '1305020305' or
B.ITEMNUM = '3108021000' or
B.ITEMNUM = '4006009940' or
B.ITEMNUM = '4006009980' or
B.ITEMNUM = '4007050480') AND (B.LOCATION = '18') AND (A.STATUS != 'CLOSE')