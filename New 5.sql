SELECT A.ITEMNUM,
       A.LOCATION, 
       B.ITEMGROUP,
       A.INVCLASS,
       A.STATUS,
       A.BINNUM,
       A.ISSUEUNIT,
       A.ABCTYPE,
       A.CCF,
       A.MINLEVEL,
       A.DELIVERYTIME,
       A.ORDERQTY,
       A.BUYERID
FROM INVENTORY A
LEFT JOIN ITEM B
ON (A.ITEMNUM = B.ITEMNUM)

